package jatek;

class Equipment {
    String name;
    String description;
    int UUID;

    public Equipment(String name, String description) {
        this.name = name;
        this.description = description;
    }

    public Equipment(String name, String description, int UUID) {
        this.name = name;
        this.description = description;
        this.UUID = UUID;
    }

    
    
    
}
